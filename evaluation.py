    def evaluate(self, bit_board):
        piece_values = self.piece_values
        white_pieces = bit_board[:6]
        black_pieces = bit_board[6:]
        white_score = 0
        black_score = 0
        for i in range(6):
            white_score += np.sum(white_pieces[i] * piece_values[i])
            black_score += np.sum(black_pieces[i] * piece_values[i])
            white_score += np.sum(white_pieces[i] * self.piece_square_table[i])
            black_score += np.sum(black_pieces[i] * np.flip(self.piece_square_table[i], axis=0))
            
        return white_score - black_score
